package 조건문;

public class IF문1 {
	public static void main(String[] args) {
		int x = 0;
		
		//조건은 비교하는 식, 결과는 true or false
		if (x == 0) { 
			System.out.println("내가 실행이 되겠지요?");
		}else {
			System.out.println("나는 조건이 맞지 않을때 실행됨.");
		}
		
		if (x == 1) {
			System.out.println("나는 실행 될까요?");
		}
	}
}
