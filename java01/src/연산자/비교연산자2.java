package 연산자;

public class 비교연산자2 {

	public static void main(String[] args) {
		// 비교연산자(==, !=, >, >=)
		// 비교 결과가 중요(비교 결과는 반드시 논리형 데이터로)
		// 기본형 데이터가 아니면 비교연산자 사용 불가능
		int id1 = 1111;
		int id2 = 2222;
		
		int pw1 = 3333;
		int pw2 = 3333;
		
		System.out.println((id1 == id2));
		System.out.println((pw1 == pw2));

	}

}
